La promesse du froid, par Argel7 ®

Appliquer une poche de glace sur un muscle ou une articulation ? C’est habituel, mais pas toujours très pratique.

Argel7® est un gel de massage à effet froid, qui apaise la peau quand les mouvements du massage peuvent causer des échauffements. Sa formule a été spécialement étudiée pour faciliter le massage des muscles et des articulations, sans l’inconfort de certaines sensations liées à la pression des paumes sur la peau. 

Les preuves scientifiques
Étude clinique de l’effet froid

Afin d’évaluer « l’effet froid » ressenti après une application unique d’Argel 7 ®, nous avons réalisé une étude dans un laboratoire indépendant, sur 21 volontaires sous contrôle dermatologique. Les changements thermiques cutanés ont été enregistrés par thermographie infrarouge pendant 30 minutes. Le froid est représenté par le bleu.

Une étude(1) le prouve : 

    La baisse de température au niveau de la peau est de 2,7°C !
    Plus de 9 personnes sur 10 qui utilisent Argel7® trouvent que cet effet froid est agréable. 
    En plus, cet effet froid est durable ! Plus d’un tiers des personnes interrogées l’ont ressenti pendant plus de 30 minutes. 

(1) Etude BIOPHYDERM 2016: 964/1016, réalisée en laboratoire, sur 21 volontaires (11 femmes et 10 hommes) âgés de plus de 50 ans. L’évaluation de l’effet rafraîchissant d’Argel7®  s’est faite sous contrôle d’un dermatologue.

 
