Les ingrédients utilisés dans la formule d’Argel7®

” Pour assurer le meilleur de votre confort lors du massage,
nous avons sélectionné 7 ingrédients d’origine naturelle et de grande qualité.”
L’argile verte

    Elle doit sa couleur aux minéraux qui la composent. Egalement appelée « illite », ce nom viendrait de l’Illinois, un Etat américain où elle a été étudiée. Mais cette argile est également très présente dans la Méditerranée.

La prêle

    La prêle pousse sur les sols sablonneux, au bord des étangs par exemple, depuis 250 millions d’années. Sa forme longiligne lui doit son surnom de « queue de rat » ou encore « queue de cheval ».

L’harpagophytum

    Cette plante qui pousse dans les déserts africains est surnommée la « griffe du diable » à cause de ses extrémités recourbées. Ces petits crochets viennent fréquemment se fixer aux sabots des chevaux. Les mouvements qu’ils font pour s’en défaire peuvent alors laisser penser qu’ils sont possédés par le diable. En réalité, cette plante est complètement inoffensive !

Le thym

    On reconnaît son odeur entre mille. L’huile essentielle qui est tirée de cette plante, si caractéristique, apporte cette touche ensoleillée au bouquet olfactif d’Argel 7®.

Le romarin

    Cultivé largement sur le pourtour de la Méditerranée, le romarin tire son nom du grec « rhops myrinos » qui signifie « buisson odorant ». Il faut dire qu’avec l’huile essentielle qu’on peut en extraire, ce nom est complètement justifié !

Le cajeput

    A l’origine, le cajeput pousse dans le delta du Mékong, au Vietnam. On peut en tirer une huile essentielle à l’odeur caractéristique. Celle-ci fait partie de la recette d’onguents reconnus dans la tradition chinoise.

La menthe poivrée

    Il existe 18 espèces de menthe. La menthe poivrée est l’une d’elle. Elle est utilisée depuis l’Antiquité, comme l’attestent les feuilles retrouvées dans des pyramides égyptiennes. Son odeur si caractéristique, qu’on retrouve dans son huile essentielle, est froide et forte.

