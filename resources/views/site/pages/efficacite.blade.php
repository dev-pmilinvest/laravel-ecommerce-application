INTERVIEW DE NOTRE EXPERT KINE

L’automassage, c’est quoi au juste ?

C’est une technique de massage qui a un but bien précis : détendre les tensions musculaires et les raideurs d’une zone ciblée. Chacun(e) peut la pratiquer seul(e) sans besoin d’aide extérieure, d’où son nom « automassage ». C‘est un avantage appréciable car ainsi, dès qu’une gêne apparaît, vous pouvez intervenir sans attendre. 

Est-ce que cela fait mal ?

Il faut bien le dire : ce massage méthodique ressemble plus à un « pétrissage » qu’à une « caresse ». Donc, pour éviter les sensations de tiraillements et les échauffements lors du massage, le mieux est de vous aider d’un gel adapté pour faciliter le glissement des mains. Idéalement, je vous recommande un gel à « effet froid ».  

Un « effet froid » ? Pourquoi ?

En baissant localement la température au niveau de la peau, le froid atténue le signal perçu par les cellules transmettant la douleur au cerveau. D’ailleurs, ce principe est utilisé dans les milieux sportifs : quand un footballeur reçoit un coup violent sur le tibia, son masseur utilise une bombe de froid qui le remet aussitôt sur pied. 

Peut-on pratiquer l’automassage à tout âge ?

Bien sûr. Et c‘est tout l‘intérêt de l’automassage, car les personnes « séniors » sont souvent concernées par les gênes articulaires et musculaires dès qu’elles se mettent en mouvement… Toutefois, il n’est pas toujours facile pour elles d’atteindre certaines zones du corps (le dos, les chevilles…).
